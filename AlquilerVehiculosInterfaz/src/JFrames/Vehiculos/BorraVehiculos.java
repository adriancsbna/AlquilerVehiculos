    private void BorraVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorraVehiculoActionPerformed
        try {
            this.matriculaVehiculo.setText(this.listaVehiculosInterfaz.datoSeleccionado());
            String matricula = matriculaVehiculo.getText();
            matricula = LecturaDatosMenu.compruebaMatricula(matricula);
            Vehiculo vehiculo = this.listaVehiculos.buscaVehiculo(matricula);//busca vehiculo por matricula
            Alquiler alquiler = vehiculo.getAlquiler();//obtiene el alquiler del vehiculo
            if (alquiler != null) {//si el vehiculo esta alquilado..
                Cliente cliente = alquiler.getCliente();//se obtiene cliente del alquiler

                DatosCliente datos = cliente.getDatos();// se obtienen los datos del cliente

                EliminarVehiculo ventana = new EliminarVehiculo("El vehiculo esta alquilado a: " + datos.getNombre() + " DNI: " + datos.getDni(), "Â¿Realmente quiere eliminarlo?", matricula, listaVehiculos, Escritorio,this.bd);
                Escritorio.add(ventana);
                this.dispose();
                this.listaVehiculosInterfaz.dispose();

            } else {//si el vehiculo no esta alquilado
                eliminaVehiculo(matricula);
                this.bd.eliminaVehiculoPorMatricula(matricula);
                Notificacion notificacion = new Notificacion("Vehiculo borrado");
                this.Escritorio.add(notificacion);
                notificacion.toFront();
                this.dispose();
                this.listaVehiculosInterfaz.dispose();

            }
        } catch (NoExisteException | FormatoIncorrectoException ex) {
            Notificacion notificacion = new Notificacion(ex.getMessage());
            this.Escritorio.add(notificacion);
            notificacion.toFront();

        }


    }//GEN-LAST:event_BorraVehiculoActionPerformed
    private void eliminaVehiculo(String matricula) {
        int indice = this.listaVehiculos.buscaVehiculoObtieneIndice(matricula);//obtiene el indice para poder borrar el vehiculo
        this.listaVehiculos.borraVehiculo(indice);

    }

