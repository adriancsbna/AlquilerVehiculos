    private void calculaPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculaPrecioActionPerformed
        try {
            String dni = this.dni.getText();
            if (dni.equals("")) {
                this.dni.setText(this.listaClientesInterfaz.datoSeleccionado());
                            dni=this.dni.getText();

            }
            String matricula = this.matricula.getText();
            if (matricula.equals("")) {
                this.matricula.setText(this.listaNoAlquilados.datoSeleccionado());
                            matricula=this.matricula.getText();

            }
            this.matriculaString = LecturaDatosMenu.compruebaMatricula(matricula);
            this.listaVehiculos.buscaVehiculo(this.matriculaString);

            this.vehiculo = this.listaVehiculos.buscaVehiculoNoAlquilado(this.matriculaString);

            dni = LecturaDatosMenu.leeDni(dni);
            this.cliente = this.listaClientes.buscaCliente(dni);

            Alquiler alquilerOriginal = this.vehiculo.getAlquiler();
            String dias = this.dias.getText();
            this.diasInt = Integer.parseInt(dias);

            this.diasInt = LecturaDatosMenu.compruebaDias(this.diasInt, 1, MAX_DIAS_ALQUILER);

            this.vehiculo.alquilate(this.diasInt, cliente);
            this.alquiler = this.vehiculo.getAlquiler();

            double precioAlquiler = this.vehiculo.calculaPrecioAlquiler(this.diasInt);

            precioAlquiler = this.alquiler.calculaPrecioAlquilerAplicandoDto(this.vehiculo, precioAlquiler);
            precioAlquiler = LecturaDatosMenu.redondearDecimales(precioAlquiler, 2);

            this.precioAlquiler = precioAlquiler;
            this.precio.setText(String.valueOf(precioAlquiler) + " euros");
            this.vehiculo.alquilate(alquilerOriginal);
            this.jPanel4.setVisible(true);

        } catch (NoExisteException | VehiculoOcupadoException | FormatoIncorrectoException ex) {
            Notificacion notificacion = new Notificacion(ex.getMessage());
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        } catch (NumberFormatException ex) {
            Notificacion notificacion = new Notificacion("No es un numero");
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        }


    }//GEN-LAST:event_calculaPrecioActionPerformed

    private void alquilaVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alquilaVehiculoActionPerformed

        try {
            double pagoR = Double.parseDouble(this.pago.getText());
            LecturaDatosMenu.consultaPagado(pagoR, this.precioAlquiler);
            this.vehiculo.alquilate(this.alquiler);

            this.vehiculo.realizaPagoSobreAlquiler(pagoR);
            this.bd.alquilaVehiculo(this.cliente, this.vehiculo,pagoR, this.diasInt);
            this.listaNoAlquilados.dispose();
            this.listaClientesInterfaz.dispose();
            this.dispose();
            Notificacion notificacion = new Notificacion("Vehiculo alquilado");
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        } catch (FormatoIncorrectoException ex) {
            Notificacion notificacion = new Notificacion(ex.getMessage());
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        } catch (NumberFormatException ex) {
            Notificacion notificacion = new Notificacion("No es un numero");
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        }


    }//GEN-LAST:event_alquilaVehiculoActionPerformed

