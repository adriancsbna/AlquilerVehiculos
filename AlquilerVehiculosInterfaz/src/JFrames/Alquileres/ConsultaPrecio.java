    private void ConsultarPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultarPrecioActionPerformed

        try {
            String matricula = this.matricula.getText();
            if (matricula.equals("")) {
                this.matricula.setText(this.listaNoAlquilados.datoSeleccionado());
                            matricula=this.matricula.getText();

            }
            this.matriculaString = LecturaDatosMenu.compruebaMatricula(matricula);
            
            this.listaVehiculos.buscaVehiculo(this.matriculaString);
            // Se considera que no se puede alquilar un vehÃ­culo mÃ¡s de 365 dÃ­as
            String dias = this.dias.getText();
            this.diasInt = Integer.parseInt(dias);

            this.diasInt = LecturaDatosMenu.compruebaDias(this.diasInt, 1, MAX_DIAS_ALQUILER);

            if (this.diasInt != null) {
                muestraResultadoDeObtenerPrecioAlquiler(this.matriculaString, this.diasInt);
                this.listaClientesInterfaz = new ListaClientesInterfaz();
                this.listaClientes.informacionDeTodosLosClientes(this.listaClientesInterfaz);
                this.Escritorio.add(this.listaClientesInterfaz);

            }
        } catch (FormatoIncorrectoException ex) {
            Notificacion notificacion = new Notificacion(ex.getMessage());
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        } catch (NumberFormatException ex) {
            Notificacion notificacion = new Notificacion("No es un numero");
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        } catch (NoExisteException ex) {
            Notificacion notificacion = new Notificacion(ex.getMessage());
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        }


    }//GEN-LAST:event_ConsultarPrecioActionPerformed

    private void AlquilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlquilarActionPerformed

        Alquiler alquilerOriginal = this.vehiculo.getAlquiler();
        if (alquilerOriginal == null) {

            try {

                double pago = Double.parseDouble(this.pagado.getText());
                LecturaDatosMenu.consultaPagado(pago, this.precioFinal);

                this.vehiculo.alquilate(this.diasInt, cliente);
                this.vehiculo.realizaPagoSobreAlquiler(pago);
                this.bd.alquilaVehiculo(this.cliente,this.vehiculo,pago,this.diasInt);
                Notificacion notificacion = new Notificacion("Vehiculo alquilado");
                this.Escritorio.add(notificacion);
                notificacion.toFront();
                this.listaNoAlquilados.dispose();
                this.listaClientesInterfaz.dispose();
                this.dispose();
            } catch (FormatoIncorrectoException ex) {
                Notificacion notificacion = new Notificacion(ex.getMessage());
                this.Escritorio.add(notificacion);
                notificacion.toFront();
            }
        } else {
            try {
                throw new VehiculoOcupadoException("El vehiculo esta alquilado");
            } catch (VehiculoOcupadoException ex) {
                Notificacion notificacion = new Notificacion(ex.getMessage());
                this.Escritorio.add(notificacion);
                notificacion.toFront();

            }
        }


    }//GEN-LAST:event_AlquilarActionPerformed

    private void ConsultaPrecioFinalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultaPrecioFinalActionPerformed
        try {
            dni.setText(this.listaClientesInterfaz.datoSeleccionado());
            this.vehiculo = this.listaVehiculos.buscaVehiculoNoAlquilado(this.matriculaString);

            String dni = this.dni.getText();
            dni = LecturaDatosMenu.leeDni(dni);
            this.cliente = this.listaClientes.buscaCliente(dni);
            Alquiler alquilerOriginal = this.vehiculo.getAlquiler();

            this.vehiculo.alquilate(this.diasInt, cliente);
            Alquiler alquiler = this.vehiculo.getAlquiler();

            double precioAlquiler = this.vehiculo.calculaPrecioAlquiler(this.diasInt);

            precioAlquiler = alquiler.calculaPrecioAlquilerAplicandoDto(this.vehiculo, precioAlquiler);
            precioAlquiler = LecturaDatosMenu.redondearDecimales(precioAlquiler, 2);

            this.precioFinal = precioAlquiler;
            this.PrecioFinal.setText(String.valueOf(precioAlquiler) + " euros");
            this.vehiculo.alquilate(alquilerOriginal);
            this.Alquilar.setVisible(true);
            this.jLabel5.setVisible(true);
            this.pagado.setVisible(true);

        } catch (NoExisteException | VehiculoOcupadoException | FormatoIncorrectoException ex) {
            Notificacion notificacion = new Notificacion(ex.getMessage());
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        } catch (NumberFormatException ex) {
            Notificacion notificacion = new Notificacion("No es un numero");
            this.Escritorio.add(notificacion);
            notificacion.toFront();
        }


    }//GEN-LAST:event_ConsultaPrecioFinalActionPerformed

